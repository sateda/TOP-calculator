function add(a, b) {
    return a + b;    
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

const resultAdd = add(1,2);
const resultSubtract = subtract(2,1);
const resultMultiply = multiply(2,2);
const resultDivide = divide(4,2);

